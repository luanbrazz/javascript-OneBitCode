let velocity = 80

switch(velocity * 2) {
    case 100:
        console.log("Sua velocidade é 100km/s")
        break
    case 160:
        console.log("Sua velocidade é 160km/s")
        break
    default:
        console.log("Velocidade não identificada")
}
/* >>Sua velocidade é 160km/s */


